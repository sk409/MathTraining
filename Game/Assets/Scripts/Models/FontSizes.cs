using UnityEngine;

public class FontSizes
{

    public static int Small
    {
        get
        {
            return (int)(Screen.width * 0.02f);
        }
    }

    public static int Medium
    {
        get
        {
            return (int)(Screen.width * 0.030f);
        }
    }

    public static int Large
    {
        get
        {
            return (int)(Screen.width * 0.036f);
        }
    }

}
